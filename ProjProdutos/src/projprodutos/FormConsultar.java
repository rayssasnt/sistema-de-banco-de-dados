    private void buscarProduto() {

        String txt = textId.getText();

        if (txt.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite um ID!");
            return;
        }

        int id = Integer.parseInt(txt);

        ControleProduto c_prod = new ControleProduto();
        Produto p = c_prod.Consultar(id);

        DefaultTableModel modelo = (DefaultTableModel) tableprodutos.getModel();
        modelo.setNumRows(0); // limpa tabela

        if (p != null) {
            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                p.getQuant(),
                p.getValor()
                
            });
        } else {
            JOptionPane.showMessageDialog(null, "Produto nÃ£o encontrado!");
        }
}

    public void carregarTabela() {

    DefaultTableModel modelo = (DefaultTableModel) tableprodutos.getModel();
    modelo.setNumRows(0); // limpa a tabela

    
    ControleProduto c_prod = new ControleProduto();

    for (Produto p: c_prod.Listar()){
        
        modelo.addRow(new Object[]{
            p.getId(),
            p.getNome(),
            p.getQuant(),
            p.getValor()
        });
    }
    }
    
 


 

