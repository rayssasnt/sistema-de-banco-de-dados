    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        // TODO add your handling code here:
        
        int id = Integer.parseInt(textid.getText());

        Produto p = c_prod.Consultar(id);

        if (p != null){
            
            textNome.setText(p.getNome());
            textQuant.setText(String.valueOf(p.getQuant()));
            textValor.setText(String.valueOf(p.getValor()));
        } else {
            JOptionPane.showMessageDialog(null, "Produto n√£o encontrado!");
            textNome.setText("");
            textQuant.setText("");
            textValor.setText("");
        }
    }//GEN-LAST:event_btBuscarActionPerformed

    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // TODO add your handling code here:
        Produto p = new Produto();
        p.setId(Integer.parseInt(textid.getText()));
        p.setNome(textNome.getText());
        p.setQuant(Integer.parseInt(textQuant.getText()));
        p.setValor(Double.parseDouble(textValor.getText()));

        if (c_prod.Alterar(p)) {
            JOptionPane.showMessageDialog(null, "Produto atualizado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao atualizar. Verifique o ID.");
        }
        
    }//GEN-LAST:event_btSalvarActionPerformed

